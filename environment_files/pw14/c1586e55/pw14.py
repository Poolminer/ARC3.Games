BACKGROUND_COLOR = 0
PADDING_COLOR = 4

class Pw14(ARCBaseGame):
    """Click-to-remove puzzle game with seeded random sprite generation."""

    hp_multiplier = 4
    hp_buffer = 4

    def __init__(self, seed: int = 0) -> None:
        self._rng = random.Random(seed)
        # Create camera with background and padding colors
        camera = Camera(
            background=BACKGROUND_COLOR,
            letter_box=PADDING_COLOR,
            width=8,
            height=8,
        )

        super().__init__(
            game_id="pw14",
            levels=levels,
            camera=camera,
            available_actions=[1, 2, 3, 4]
        )

    def on_set_level(self, level: Level) -> None:
        self.hp_buffer = self.hp_multiplier

    def _check_win(self) -> bool:
        goals = self.current_level.get_sprites_by_tag('g')
        goal_objects = self.current_level.get_sprites_by_tag('go')

        num_correct = 0

        for go in goal_objects:
            for g in goals:
                if g.name[1:] == go.name[1:] and go.x == g.x and go.y == g.y:
                    num_correct += 1

        return num_correct == len(goal_objects)
    
    def get_agent_sprite(self):
        return self.current_level.get_sprites_by_name('a')[0]
    
    def is_connected(self, sprite: Sprite, other: Sprite, dx: int, dy: int):
        sprite._x += dx
        sprite._y += dy

        connected = sprite.collides_with(other)

        sprite._x -= dx
        sprite._y -= dy

        return connected
    
    def get_connected_sprites(self, sprite: Sprite, dx: int, dy: int):
        connected = []

        for other in self.current_level.get_sprites():
            if other == sprite:
                continue

            if self.is_connected(sprite, other, dx, dy):
                connected.append(other)

        return connected

    def move_agent(self, agent: Sprite, dx: int, dy: int):
        unit = [agent]

        new_targets = [agent]

        connected = self.get_connected_sprites(agent, dx, dy)

        for sprite in connected:
            if 'aw' in sprite.tags:
                return

        while len(new_targets) != 0:
            _new_targets = []

            for target in new_targets:
                connected = self.get_connected_sprites(target, dx, dy)
                connected = [s for s in connected if not s in unit]
                connected = [s for s in connected if not 'aw' in s.tags]
                _new_targets += connected
            unit += _new_targets
            new_targets = _new_targets
        
        for sprite in unit:
            if 'w' in sprite.tags:
                return
        
        for sprite in unit:
            sprite._x += dx
            sprite._y += dy

    def step(self) -> None:
        """Process game logic for each step."""
        # Handle click action (ACTION6)

        agent = self.current_level.get_sprites_by_name('a')[0]

        match self.action.id:
            case GameAction.ACTION1:
                self.move_agent(agent, 0, -3)
            case GameAction.ACTION2:
                self.move_agent(agent, 0, 3)
            case GameAction.ACTION3:
                self.move_agent(agent, -3, 0)
            case GameAction.ACTION4:
                self.move_agent(agent, 3, 0)

        lost = False

        if self.action.id != GameAction.RESET:
            self.hp_buffer -= 1

            if self.hp_buffer == 0:
                self.hp_buffer = self.hp_multiplier

                hb = self.current_level.get_sprites_by_name('hb2')[0]

                hp = hb.width - 1

                for x in range(hb.width):
                    if hb.pixels[0][hb.width - 1 - x] == -1:
                        hp -= 1
                
                hb.pixels[0][hp] = -1

                if hp == 0:
                    lost = True
                    self.lose()

        # Check win condition
        if not lost and self._check_win():
            self.next_level()

        self.complete_action()