# Type aliases
Point = Tuple[int, int]
State = Tuple[Point, ...]

class Colors:
    """An enumeration of all colors involved in rendering a PushWorld puzzle."""

    AGENT = 14
    AGENT_BORDER = 14
    AGENT_WALL = 11
    AGENT_WALL_BORDER = 11
    GOAL = -2  # transparent
    GOAL_BORDER = 13
    GOAL_OBJECT = 8
    GOAL_OBJECT_BORDER = 1
    MOVABLE = 9
    MOVABLE_BORDER = 10
    WALL = 5
    WALL_BORDER = 5
    HEALTH_BAR_1 = 2
    HEALTH_BAR_2 = 15

@dataclass(frozen=True)
class PushWorldObject:
    """An object in the the PushWorld environment.

    Attributes:
        position: The absolute position of the object.
        fill_color: The color with which to fill the object's area when rendered.
        border_color: The color of the object's border when rendered.
        cells: The discrete positions that this object occupies, defined relative to
            the object's frame.
    """

    position: Point
    fill_color: int
    cells: Set[Point]

class PushWorldPuzzle:
    def __init__(self, pwp: str) -> None:
        obj_pixels = defaultdict(set)

        elems_per_row = -1
        lines = pwp.splitlines()[1:-1]

        self.sprites = {}

        for line_idx, line in enumerate(lines):
            y = line_idx + 1
            line_elems = line.split()
            if y == 1:
                elems_per_row = len(line_elems)
            else:
                if elems_per_row != len(line_elems):
                    raise ValueError(
                        f"Row {y} does not have the same number of elements as "
                        "the first row."
                    )

            for x in range(1, len(line_elems) + 1):
                cell_elems = line_elems[x - 1].split("+")
                for elem_id in cell_elems:
                    elem_id = elem_id.lower()
                    if elem_id != ".":
                        obj_pixels[elem_id].add((x, y))

        if "a" not in obj_pixels:
            raise ValueError(
                "Every puzzle must have an agent object, indicated by 'a'."
            )

        # Add walls at the boundaries of the puzzle
        width = self.width = x + 2
        height = self.height = y + 2

        for xx in range(width):
            obj_pixels["w"].add((xx, 0))
            obj_pixels["w"].add((xx, height - 1))
        for yy in range(height):
            obj_pixels["w"].add((0, yy))
            obj_pixels["w"].add((width - 1, yy))

        movables = ["a"]
        self._goal_state = ()
        object_positions = {}
        self._movable_objects = []
        self._goals = []

        num_walls = 0
        num_agent_walls = 0

        for elem_id in list(obj_pixels.keys()):
            pixels = obj_pixels[elem_id]

            xx, yy = zip(*pixels)
            position = (min(xx), min(yy))

            pixels = subtract_from_points(pixels, position)
            object_positions[elem_id] = position
            obj_pixels[elem_id] = pixels

            if elem_id == "w":
                name = f"w{num_walls}"
                num_walls += 1
                self.sprites[name] = Sprite(pixels=points_to_grid(pixels, Colors.WALL, Colors.WALL_BORDER), name=name, x=position[0]*3, y=position[1]*3, visible=True, collidable=True, tags=['wall', 'w'])
            elif elem_id == "aw":
                name = f"aw{num_agent_walls}"
                num_agent_walls += 1
                self.sprites[name] = Sprite(pixels=points_to_grid(pixels, Colors.AGENT_WALL, Colors.AGENT_WALL_BORDER), name=name, x=position[0]*3, y=position[1]*3, visible=True, collidable=True, tags=['wall','aw'])
            elif elem_id == "a":
                self.sprites['a'] = Sprite(pixels=points_to_grid(pixels, Colors.AGENT, Colors.AGENT_BORDER), name="a", x=position[0]*3, y=position[1]*3, visible=True, collidable=True)
            elif elem_id[0] == "g":
                self.sprites[elem_id] = Sprite(pixels=points_to_grid(pixels, Colors.GOAL, Colors.GOAL_BORDER), name=elem_id, x=position[0]*3, y=position[1]*3, visible=True, collidable=False, tags=['g'])

            if elem_id[0] == "g":
                self._goal_state += (object_positions[elem_id], )
                movable_id = "m" + elem_id[1:]
                assert (
                    movable_id in obj_pixels
                ), f"Goal has no associated movable object: {movable_id}"
                movables.append(movable_id)

        for elem_id in obj_pixels:
            if elem_id[0] == "m" and elem_id not in movables:
                movables.append(elem_id)

        for i, elem_id in enumerate(movables[1:]):
            pixels = obj_pixels[elem_id]
            position = object_positions[elem_id]
            color = Colors.MOVABLE if i >= len(self._goal_state) else Colors.GOAL_OBJECT
            is_goal_object = i < len(self._goal_state)
            border_color = Colors.MOVABLE_BORDER if not is_goal_object else Colors.GOAL_OBJECT_BORDER
            self.sprites[elem_id] = Sprite(pixels=points_to_grid(pixels, color, border_color), name=elem_id, x=position[0]*3, y=position[1]*3, visible=True, collidable=True, tags=['m', 'go'] if is_goal_object else ['m'])
        
        self.sprites['hb1'] = Sprite(pixels=[[Colors.HEALTH_BAR_1] * (width * 3)], name='hb1', x=0, y=height*3-1, visible=True, collidable=False, tags=['hb'])
        self.sprites['hb2'] = Sprite(pixels=[[Colors.HEALTH_BAR_2] * (width * 3)], name='hb2', x=0, y=height*3-1, visible=True, collidable=False, tags=['hb'])

def subtract_from_points(points: Set[Point], offset: Point) -> Set[Point]:
    """Returns the set {p - offset} for all `p` in `points`."""
    dx, dy = offset
    return set((x - dx, y - dy) for x, y in points)

def min_point(points: Set[Point]):
    min_x = math.inf
    min_y = math.inf

    for point in points:
        if point[0] < min_x:
            min_x = point[0]
        if point[1] < min_y:
            min_y = point[1]
    return (min_x, min_y)

def max_point(points: Set[Point]):
    min_x = -math.inf
    min_y = -math.inf

    for point in points:
        if point[0] > min_x:
            min_x = point[0]
        if point[1] > min_y:
            min_y = point[1]
    return (min_x, min_y)

def expand(points: Set[Point]):
    return set((x * 3, y * 3) for x, y in points)

def points_to_grid(points: Set[Point], color: int, outline_color: int, dotted_outline = False, offset_dottet_outline = False, secondary_outline_color = -1):
    points = expand(points)

    min_pt = min_point(points)
    max_pt = max_point(points)

    width = 3 + max_pt[0] - min_pt[0]
    height = 3 + max_pt[1] - min_pt[1]

    grid = []

    for y in range(height):
        grid.append([-1] * width)

    for point in points:
        x = point[0] + 1
        y = point[1] + 1

        grid[y-1][x-1] = outline_color
        grid[y-1][x] = outline_color
        grid[y-1][x+1] = outline_color

        grid[y+1][x-1] = outline_color
        grid[y+1][x] = outline_color
        grid[y+1][x+1] = outline_color

        grid[y][x-1] = outline_color
        grid[y][x-1] = outline_color
        grid[y][x+1] = outline_color

        grid[y][x+1] = outline_color
        grid[y][x+1] = outline_color
        grid[y][x+1] = outline_color
    
    for point in points:
        x = point[0] + 1
        y = point[1] + 1
        
        grid[y][x] = color

    for x in range(width-3):
        for y in range(height):
            if grid[y][x] == color:
                if grid[y][x+1] == outline_color and grid[y][x+2] == outline_color and grid[y][x+3] == color:
                    grid[y][x+1] = color
                    grid[y][x+2] = color

    for x in range(width):
        for y in range(height-3):
            if grid[y][x] == color:
                if grid[y+1][x] == outline_color and grid[y+2][x] == outline_color and grid[y+3][x] == color:
                    grid[y+1][x] = color
                    grid[y+2][x] = color

    if dotted_outline:
        for x in range(width):
            for y in range(height):
                if grid[y][x] == outline_color and ((x % 2 == 0 and y % 2 == 0) or (x % 2 == 1 and y % 2 == 1) if offset_dottet_outline else (x % 2 == 0 and y % 2 == 1) or (x % 2 == 1 and y % 2 == 0)):
                    if grid[y][x] == outline_color:
                        grid[y][x] = secondary_outline_color
                
    return grid

def bounding_rect(points: Set[Point], color: int):
    min_pt = min_point(points)
    max_pt = max_point(points)

    width = 3 + max_pt[0] - min_pt[0]
    height = 3 + max_pt[1] - min_pt[1]

    grid = []

    for y in range(height):
        grid.append([-1] * width)

    for xx in range(width):
        grid[0][xx] = color
        grid[height - 1][xx] = color
    for yy in range(height):
        grid[yy][0] = color
        grid[yy][width - 1] = color
    
    return grid

